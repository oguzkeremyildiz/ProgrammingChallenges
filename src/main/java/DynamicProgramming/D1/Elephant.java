public class Elephant {

    private int weight;
    private int iq;
    private int no;

    public Elephant(int weight, int iq, int no) {
        this.weight = weight;
        this.iq = iq;
        this.no = no;
    }

    public int getWeight() {
        return weight;
    }

    public int getIq() {
        return iq;
    }

    public int getNo() {
        return no;
    }
}
