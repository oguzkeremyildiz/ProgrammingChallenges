public class Chopstick {

    private int index;
    private int chopstick;

    public Chopstick(int index, int chopstick) {
        this.index = index;
        this.chopstick = chopstick;
    }

    public int getIndex() {
        return index;
    }

    public int getChopstick() {
        return chopstick;
    }
}
